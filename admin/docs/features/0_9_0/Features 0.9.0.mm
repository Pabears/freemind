		def child2 = it.next(); 
		sum += calcWork(child2);
		def w = child2.getAttribute(&quot;work&quot;);
		if(w != null)
			sum += Integer.parseInt( w);
	}
	if(sum&gt;0)
		c.editAttribute(child, &quot;sum&quot;, (String) sum);
	return sum;
}

